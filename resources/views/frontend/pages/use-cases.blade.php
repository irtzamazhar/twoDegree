                <p><strong>DOCTOR:</strong> Julie gets her teeth cleaned at Jordan's Dentistry. Jordan's Dentistry sends a message to all of Julie's friends who are geo-fenced within 5 miles of the dentist's office. If Julie's friends redeem the "free teeth cleaning for new customers who are friends with Julie" deal, Julie's next visit to the dentist is free. The dentist's office gets 10 new patients to sign-up through Julie. Julie gets a free cleaning and check-up – and all of Julie's friends now go to the same dentist as her. The dentists sends the deal to all 10 patients' friends.</p>

